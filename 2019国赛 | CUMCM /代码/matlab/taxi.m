x = 0:0.1:30;

y1 = 14;
y2 = 14 + 2.5*(x-3);
y3 = 14 + 2.5*12 + 3.6*(x-15);

y_day = (y1).*(x>=0 & x<=3) + (y2).*(x>3 & x<=15) + (y3).*(x>15);
figure;
plot(x, y_day);

hold on;

y4 = 18;
y5 = 18 + 3.1*(x-3);
y6 = 18 + 3.1*12 + 4.7*(x-15);
y_night = (y4).*(x>=0 & x<=3) + (y5).*(x>3 & x<=15) + (y6).*(x>15);

plot(x, y_night);
legend('白天（5:00-23:00）','夜间（23:00-次日5:00）');

xlabel('里程/km');
ylabel('价格/元');
title('出租车计费图像');